#include<bits/stdc++.h>
using namespace std;

long long q;

bool solution(string a, string b)
{
    for(int i = 0,j = 0; i < a.length(); ++i)
    {
        if(a[i] == b[j])
        {
            ++j;
            if(j == b.length())
            {
                return true;
            }
        }
    }
    return false;
}
int main()
{
    string b = "hello";
    cin >> q;
    while(q--)
    {
        string a;
        cin >> a;
        if( solution(a,b) == true) cout << "YES" << "\n";
        else cout << "NO" << "\n";
    }
    return 0;
}
