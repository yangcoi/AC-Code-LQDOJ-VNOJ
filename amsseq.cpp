#include <bits/stdc++.h>
using namespace std;

int f[10100],n,k,x,ans=0;
int main()
{

    cin >> n >> k;
    for (int i=1;i<=n;i++)
    {
        cin >> x;
        f[i]=-1e9;
        for (int j=1;j<=k;j++)
            if (i-j>=0)
                f[i]=max(f[i],f[i-j]+x);
        ans=max(ans,f[i]);
    }
    cout << ans ;
}
