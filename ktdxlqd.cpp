#include<bits/stdc++.h>
using namespace std;

int main()
{
    string s, t;
    getline(cin, s);
    getline(cin, t);

    int res = 0;
    int n = s.size();
    for (int i = 0; i < n; ++i)
        res += (s[i] == t[n - i - 1]);
    cout << res;
    return 0;
}

