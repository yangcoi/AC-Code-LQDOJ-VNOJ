#include <bits/stdc++.h>
using namespace std;

int main()
{
    vector<string> res = {
        "00:00", "01:10", "02:20", "03:30", "04:40", "05:50", "10:01", "11:11",
        "12:21", "13:31", "14:41", "15:51", "20:02", "21:12", "22:22", "23:32"
    };

    string time;
    cin >> time;

    if (time >= res.back())
    {
        cout << res.front();
        return 0;
    }

    int n = int(res.size());
    for (int i = n - 1; i >= 1; --i)
    {
        if (res[i] > time && time >= res[i - 1])
        {
            cout << res[i];
            return 0;
        }
    }
    return 0;
}


