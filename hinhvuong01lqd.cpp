#include<bits/stdc++.h>
using namespace std;

#define ll long long
#define mod 20192020
ll i,j,K;

ll G(ll x)
{
    ll res;
    if(x%2==0) 
        res=(x/2)%mod*(x+1)%mod;
    else 
        res=((x+1)/2)%mod*x%mod;
    return res;
}

int main()
{
   cin>>i>>j>>K;
   ll kq;
   kq=(G(i+K-1)-G(i-1)+mod)%mod;
   kq=kq*(G(j+K-1)-G(j-1)+mod)%mod;
   cout<<kq;
}
