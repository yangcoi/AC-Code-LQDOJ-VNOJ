#include<bits/stdc++.h>
using namespace std;

int main()
{
    string a, b;
    cin>>a>>b;
    int n = a.size();
    int m = b.size();

    for (int i = 0, j = 0; i < n; ++i)
    {
        if (a[i] == b[j])
        {
            if (m == ++j)
            {
                cout << "YES";
                return 0;
            }
        }
    }
    cout << "NO";
    return 0;
}


