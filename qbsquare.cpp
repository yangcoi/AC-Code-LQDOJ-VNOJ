#include <bits/stdc++.h>
using namespace std;
int n, m, a[1002][1002], f[1002][1002], ans;

int main()
{
    cin >> m >> n;
    for (int i=1; i<=m; ++i)
        for (int j=1; j<=n; ++j) cin >> a[i][j];

    for (int i=1; i<=m; ++i) f[i][1] = 1;
    for (int j=1; j<=n; ++j) f[1][j] = 1;
    for (int i=2; i<=m; ++i)
        for (int j=2; j<=n; ++j)
        {
            if (a[i][j]==a[i-1][j] && a[i][j]==a[i][j-1] && a[i][j]==a[i-1][j-1])
                f[i][j]=min(f[i-1][j],min(f[i][j-1],f[i-1][j-1]))+1;
            else f[i][j]=1;
            ans = max(ans, f[i][j]);
        }
    cout << ans;
}
