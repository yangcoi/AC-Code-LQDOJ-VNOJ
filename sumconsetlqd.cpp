#include<bits/stdc++.h>
using namespace std;

int n, t, ans, a[100005];
bool cmp(int x, int y)
{
    return x < y;
}
int main()
{
    cin >> n >> t;
    for(int i = 1; i <= n; ++i)
    {
        cin >> a[i];
        a[i] = a[i-1] + a[i];
    }
    for(int i = 1; i <= n; ++i)
    {
        int k = upper_bound(a+i+1, a+n+1, t+a[i-1], cmp) - a;
        ans = max(ans, (k-1) - i + 1);
    }
    cout  << ans;
    return 0;
}


