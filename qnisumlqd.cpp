#include<bits/stdc++.h>
using namespace std;

long long n,res,s[1000001];
int main()
{

    cin>>n;
    for(int i=1;i<=n;i++)
        s[i]=s[i-1]+i;
    for(int i=1;i<=n;i++)
        res+=s[i];
    cout<<res;
}


