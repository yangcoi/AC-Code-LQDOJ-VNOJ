/*
Ý TƯỞNG:ta tạo mảng a[] nhằm lưu lại các giá trị i∗j (i=1→n,j=1→m), sau đó sắp xếp lại mảng rồi in ra phần tử thứ k
*/
//https://lqdoj.edu.vn/problem/bnhan
#include <bits/stdc++.h>
using namespace std;

const int maxn = 1000001;
long long n,m,k,a[maxn],s[maxn],mn;

int main()
{
    ios_base::sync_with_stdio(0);
    cin >> n >> m >> k;
    for(int i=1;i<=m;i++)
    {
        for(int j=1;j<=n;j++)
        {
            a[++mn]=i*j;
        }
    }
    sort(a+1,a+1+mn);
    cout<<a[k];
}
