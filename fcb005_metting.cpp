#include <bits/stdc++.h>
using namespace std;

#define fastIO ios_base::sync_with_stdio(false); cin.tie(NULL)
#define fi first
#define se second

signed main() {
    fastIO;
    int n;
    char c;
    cin >> n >> c;
    for (int i = 1; i <= n; i++)
    {
        string s;
        cin >> s;
        int flag = 0;
        for (int j = 0; j < s.size(); j++)
            if (s[j] == c)
            {
                flag = 1;
                break;
            }
        if (flag == 0)
            cout << s << endl;
    }
    return 0;
}














