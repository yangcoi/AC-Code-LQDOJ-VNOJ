#include<bits/stdc++.h>
using namespace std;

int main()
{

    string s;
    cin >> s;


    int mx = 0;
    map<char, int> M;
    for (char c : s) mx = max(mx, ++M[c]);


    for (pair<char, int> e : M)
        if (e.second == mx)
            return cout << e.first, 0;

    return 0;
}


